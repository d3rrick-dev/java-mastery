package phase12.INTERMEDIATE;

/**
 * INTERVIEW QUESTION: Difference between bytecode and source code?
 *
 * Difficulty: INTERMEDIATE
 */

public class Lesson24_WhatIsTheDifferenceBetweenBytecodeAndSourceCode {
    public static void main(String[] args) {
        System.out.println("=== BYTECODE VS SOURCE CODE ===\n");
        System.out.println("Source Code:");
        System.out.println("  - Human-readable Java code (.java files)");
        System.out.println("  - Written by developers");
        System.out.println("  - Contains comments, meaningful names");
        System.out.println("  - Cannot be executed directly by JVM");
        System.out.println();
        System.out.println("Bytecode:");
        System.out.println("  - Platform-independent intermediate code (.class files)");
        System.out.println("  - Generated by javac compiler");
        System.out.println("  - Executed by JVM");
        System.out.println("  - Not human-readable (opcodes)");
        System.out.println();
        System.out.println("javac compiles source -> bytecode");
        System.out.println("java (JVM) executes bytecode");
    }
}
