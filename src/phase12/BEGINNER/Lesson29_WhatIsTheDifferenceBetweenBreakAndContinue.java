package phase12.BEGINNER;

/**
 * INTERVIEW QUESTION: Difference between break and continue?
 *
 * Difficulty: BEGINNER
 */

public class Lesson29_WhatIsTheDifferenceBetweenBreakAndContinue {
    public static void main(String[] args) {
        System.out.println("=== BREAK VS CONTINUE ===\n");
        System.out.println("break: Exits the loop entirely");
        System.out.println("continue: Skips current iteration, continues with next");
        System.out.println();
        System.out.println("break: Used when condition met, no more iterations needed");
        System.out.println("continue: Used when condition met, skip but keep looping");
    }
}
